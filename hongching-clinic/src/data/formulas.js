// ─────────────────────────────────────────────────
// Helper: search formulas by name, herb, or indication
// ─────────────────────────────────────────────────
export function searchFormulas(query) {
  if (!query || typeof query !== 'string') return [];
  const q = query.trim().toLowerCase();
  if (q.length === 0) return [];

  return TCM_FORMULAS_DB.filter((f) => {
    // Match formula name
    if (f.name.toLowerCase().includes(q)) return true;
    // Match indication
    if (f.ind && f.ind.toLowerCase().includes(q)) return true;
    // Match herb names
    if (f.herbs.some((herb) => herb.h.toLowerCase().includes(q))) return true;
    // Match category
    if (f.cat.toLowerCase().includes(q)) return true;
    // Match source text
    if (f.src.toLowerCase().includes(q)) return true;
    return false;
  });
}

// ─────────────────────────────────────────────────
// Helper: get all formulas in a given category
// ─────────────────────────────────────────────────
export function getFormulasByCategory(cat) {
  if (!cat || typeof cat !== 'string') return [];
  return TCM_FORMULAS_DB.filter((f) => f.cat === cat);
}
