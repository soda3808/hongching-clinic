/**
 * Search acupoints by name, code, meridian, or indication.
 * Returns all matching acupoints. The query is case-insensitive.
 *
 * @param {string} query - search term (Chinese name, code, or keyword)
 * @returns {Array} matching acupoint objects
 */
export function searchAcupoints(query) {
  if (!query || typeof query !== 'string') return [];
  const q = query.trim().toLowerCase();
  if (q.length === 0) return [];

  return ACUPOINTS_DB.filter((pt) => {
    return (
      pt.name.includes(q) ||
      pt.code.toLowerCase().includes(q) ||
      pt.mer.includes(q) ||
      pt.merAbbr.toLowerCase().includes(q) ||
      pt.ind.includes(q) ||
      pt.loc.includes(q) ||
      pt.note.includes(q)
    );
  });
}

/**
 * Get all acupoints belonging to a specific meridian.
 *
 * @param {string} merAbbr - meridian abbreviation (e.g. 'LU', 'LI', 'ST', 'EX')
 * @returns {Array} acupoint objects for that meridian
 */
export function getAcupointsByMeridian(merAbbr) {
  if (!merAbbr || typeof merAbbr !== 'string') return [];
  const abbr = merAbbr.trim().toUpperCase();
  return ACUPOINTS_DB.filter((pt) => pt.merAbbr === abbr);
}
