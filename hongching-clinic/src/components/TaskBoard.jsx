// ── Load / Save ──
function loadTasks() {
  try {
    const raw = localStorage.getItem(STORAGE_KEY);
    if (raw) {
      const parsed = JSON.parse(raw);
      return { tasks: parsed.tasks || [], settings: { ...DEFAULT_SETTINGS, ...parsed.settings } };
    }
  } catch { /* ignore */ }
  return { tasks: [], settings: { ...DEFAULT_SETTINGS } };
}

function saveTasks(data) {
  localStorage.setItem(STORAGE_KEY, JSON.stringify(data));
}

// ── Auto Task Generation ──
function generateAutoTasks(existingTasks, data, settings) {
  const td = today();
  const tom = tomorrow();
  const dom = dayOfMonth();
  const dow = dayOfWeek();
  const newTasks = [];

  const exists = (key) => existingTasks.some(t => t.autoKey === key);
  const makeTask = (title, role, priority, autoKey, linkedPage, dueDate, dueTime) => ({
    id: uid(),
    title,
    description: '',
    dueDate: dueDate || td,
    dueTime: dueTime || null,
    priority: priority || 'normal',
    role,
    status: 'pending',
    completedAt: null,
    completedBy: null,
    recurring: 'daily',
    linkedPage: linkedPage || null,
    autoGenerated: true,
    autoKey,
    createdAt: new Date().toISOString(),
  });

  // Count data
  const todayBookings = (data.bookings || []).filter(b => b.date === td && b.status !== 'cancelled');
  const tomorrowBookings = (data.bookings || []).filter(b => b.date === tom && b.status !== 'cancelled');
  const bookingCount = todayBookings.length;
  const tomorrowCount = tomorrowBookings.length;
  const lowStockItems = (data.inventory || []).filter(i => {
    const qty = Number(i.qty || i.quantity || 0);
    const min = Number(i.minQty || i.min || i.reorderLevel || 10);
    return qty <= min;
  });
  const hasFollowUps = (data.consultations || []).some(c => {
    const fu = c.followUp || c.followUpDate;
    return fu && fu === td;
  });
  const pendingBills = (data.revenue || []).filter(r => r.status === 'unpaid' || r.status === 'pending');

  // ── 醫師 Doctor Tasks ──
  const dk = (key) => `doctor_${key}_${td}`;
  if (!exists(dk('bookings'))) {
    newTasks.push(makeTask(`查看今日預約 (${bookingCount} 位病人)`, '醫師', 'high', dk('bookings'), 'booking', td, '09:00'));
  }
  if (!exists(dk('followup')) && hasFollowUps) {
    newTasks.push(makeTask('覆診提醒跟進', '醫師', 'high', dk('followup'), 'followup', td, '09:30'));
  }
  if (!exists(dk('rxcheck'))) {
    newTasks.push(makeTask('處方藥物檢查', '醫師', 'normal', dk('rxcheck'), 'formulas', td, '10:00'));
  }
  if (!exists(dk('emrsign'))) {
    newTasks.push(makeTask('簽署病歷記錄', '醫師', 'normal', dk('emrsign'), 'emr', td, '18:00'));
  }

  // ── 護士 Nurse Tasks ──
  const nk = (key) => `nurse_${key}_${td}`;
  if (!exists(nk('prep'))) {
    newTasks.push(makeTask('開診準備：檢查診療室物資', '護士', 'high', nk('prep'), null, td, '08:30'));
  }
  if (!exists(nk('bookings'))) {
    newTasks.push(makeTask(`確認今日預約名單 (${bookingCount} 位)`, '護士', 'high', nk('bookings'), 'booking', td, '08:45'));
  }
  if (!exists(nk('vitals'))) {
    newTasks.push(makeTask('量度等候病人生命體徵', '護士', 'normal', nk('vitals'), 'vitals', td, '09:00'));
  }
  if (!exists(nk('stockcheck')) && lowStockItems.length > 0) {
    newTasks.push(makeTask(`藥材庫存檢查 (${lowStockItems.length} 項低庫存)`, '護士', 'high', nk('stockcheck'), 'inventory', td, '11:00'));
  } else if (!exists(nk('stockcheck'))) {
    newTasks.push(makeTask('藥材庫存檢查', '護士', 'normal', nk('stockcheck'), 'inventory', td, '11:00'));
  }
  if (!exists(nk('disinfect'))) {
    newTasks.push(makeTask('消毒診療室', '護士', 'normal', nk('disinfect'), null, td, '18:30'));
  }

  // ── 前台 Reception Tasks ──
  const rk = (key) => `reception_${key}_${td}`;
  if (!exists(rk('tomorrow'))) {
    newTasks.push(makeTask(`確認明日預約 (${tomorrowCount} 位) — 致電提醒`, '前台', 'high', rk('tomorrow'), 'booking', td, '15:00'));
  }
  if (!exists(rk('closing'))) {
    newTasks.push(makeTask('處理今日收銀結算', '前台', 'normal', rk('closing'), 'closing', td, '18:00'));
  }
  if (!exists(rk('unpaid'))) {
    const unpaidCount = pendingBills.length;
    newTasks.push(makeTask(`檢查未付帳單${unpaidCount > 0 ? ` (${unpaidCount} 張)` : ''}`, '前台', unpaidCount > 0 ? 'high' : 'normal', rk('unpaid'), 'arap', td, '16:00'));
  }
  if (!exists(rk('inquiries'))) {
    newTasks.push(makeTask('回覆查詢訊息', '前台', 'normal', rk('inquiries'), 'crm', td, '10:00'));
  }

  // ── 管理員 Admin Tasks ──
  const ak = (key) => `admin_${key}_${td}`;

  // Daily
  if (!exists(ak('revenue'))) {
    newTasks.push(makeTask('查看今日營業額', '管理員', 'normal', ak('revenue'), 'rev', td, '18:00'));
  }

  // Monthly: Day 1-5
  if (dom >= 1 && dom <= 5) {
    const mk = (key) => `admin_${key}_${td.substring(0, 7)}`;
    if (!exists(mk('pnl'))) {
      newTasks.push({
        ...makeTask('完成上月 P&L 損益表', '管理員', 'high', mk('pnl'), 'monthend', td, null),
        recurring: 'monthly',
      });
    }
    if (!exists(mk('payroll'))) {
      newTasks.push({
        ...makeTask('計糧 + 出糧單', '管理員', 'high', mk('payroll'), 'payroll', td, null),
        recurring: 'monthly',
      });
    }
  }
  if (dom === 1) {
    const mk = (key) => `admin_${key}_${td.substring(0, 7)}`;
    if (!exists(mk('stocktake'))) {
      newTasks.push({
        ...makeTask('庫存盤點', '管理員', 'normal', mk('stocktake'), 'stocktake', td, null),
        recurring: 'monthly',
      });
    }
  }
  if (dom === settings.rentDueDay) {
    const mk = (key) => `admin_${key}_${td.substring(0, 7)}`;
    if (!exists(mk('rent'))) {
      newTasks.push({
        ...makeTask('交租提醒', '管理員', 'high', mk('rent'), 'exp', td, null),
        recurring: 'monthly',
      });
    }
  }
  if (dom === settings.mpfDueDay) {
    const mk = (key) => `admin_${key}_${td.substring(0, 7)}`;
    if (!exists(mk('mpf'))) {
      newTasks.push({
        ...makeTask('MPF 供款提醒', '管理員', 'high', mk('mpf'), 'mpf', td, null),
        recurring: 'monthly',
      });
    }
  }

  // Weekly
  if (dow === 1) { // Monday
    const wk = `admin_social_${td}`;
    if (!exists(wk)) {
      newTasks.push({
        ...makeTask('發佈本週社交媒體內容', '管理員', 'normal', wk, null, td, null),
        recurring: 'weekly',
      });
    }
  }
  if (dow === 5) { // Friday
    const wk = `admin_weeklyreview_${td}`;
    if (!exists(wk)) {
      newTasks.push({
        ...makeTask('檢視本週業績報告', '管理員', 'normal', wk, 'report', td, null),
        recurring: 'weekly',
      });
    }
  }

  // ── 付款排程 Payment Schedule ──
  const schedule = settings.paymentSchedule || [];
  schedule.filter(s => s.active).forEach(s => {
    if (dom === s.day) {
      const pk = `payment_${s.id}_${td.substring(0, 7)}`;
      if (!exists(pk)) {
        const amtLabel = s.amount ? ` HK$${s.amount.toLocaleString()}` : '';
        newTasks.push({
          ...makeTask(`💳 ${s.label}${amtLabel}`, '管理員', 'high', pk, s.category === '人工' ? 'payroll' : 'exp', td, null),
          recurring: 'monthly',
          description: `分類：${s.category}${s.store ? ' | 分店：' + s.store : ''}${s.staff ? ' | 對象：' + s.staff : ''}`,
        });
      }
    }
  });

  return newTasks;
}

// ── Mark overdue ──
function markOverdue(tasks) {
  const td = today();
  return tasks.map(t => {
    if (t.status === 'pending' && t.dueDate < td) {
      return { ...t, status: 'overdue' };
    }
    return t;
  });
}

