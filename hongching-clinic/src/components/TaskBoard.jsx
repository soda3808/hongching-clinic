  const { overdue, todayPending, completedToday, upcoming } = useMemo(() => {
    const td = today();
    const next7 = [];
    for (let i = 1; i <= 7; i++) {
      const d = new Date();
      d.setDate(d.getDate() + i);
      next7.push(d.toISOString().substring(0, 10));
    }

    const overdue = filteredTasks.filter(t => t.status === 'overdue');
    const todayPending = filteredTasks.filter(t => t.status === 'pending' && t.dueDate === td)
      .sort((a, b) => (a.dueTime || '99:99').localeCompare(b.dueTime || '99:99'));
    const completedToday = filteredTasks.filter(t => t.status === 'completed' && t.dueDate === td);
    const upcoming = filteredTasks.filter(t => t.status === 'pending' && next7.includes(t.dueDate))
      .sort((a, b) => a.dueDate.localeCompare(b.dueDate) || (a.dueTime || '').localeCompare(b.dueTime || ''));

    return { overdue, todayPending, completedToday, upcoming };
  }, [filteredTasks]);

